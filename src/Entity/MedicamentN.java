package Entity;

/**
 *
 * @author bytesudoer
 */
public class MedicamentN {
    private int id;
    private int code;
    private String libelle;
    private float prix;
    private String type;
    private int dosage;
    private String description;
    private String nom;

     public MedicamentN() {
    }

    public MedicamentN(int id, String nom, int dosage, float prix, String description) {
        this.id = id;
        this.nom = nom;
        this.dosage = dosage;
        this.prix = prix;
        this.description = description;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public String getLibelle() {
        return libelle;
    }

    public void setLibelle(String libelle) {
        this.libelle = libelle;
    }

    public float getPrix() {
        return prix;
    }

    public void setPrix(float prix) {
        this.prix = prix;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public int getDosage() {
        return dosage;
    }

    public void setDosage(int dosage) {
        this.dosage = dosage;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getNom() {
        return nom;
    }

    public void setNom(String nom) {
        this.nom = nom;
    }

    @Override
    public String toString() {
        return "Medicament{" + "id=" + id + ", code=" + code + ", libelle=" + libelle + ", prix=" + prix + ", type=" + type + ", dosage=" + dosage + ", description=" + description + ", nom=" + nom + '}';
    }

    
    
}
